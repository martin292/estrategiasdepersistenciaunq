package tp6;

public class Destino {
	
	private String pais;
	private String ciudad;
	
	
	
	public Destino(String pais, String ciudad) {
		super();
		this.pais = pais;
		this.ciudad = ciudad;
	}
	
	
	
	public String getPais() {
		return pais;
	}
	public void setPais(String pais) {
		this.pais = pais;
	}
	public String getCiudad() {
		return ciudad;
	}
	public void setCiudad(String ciudad) {
		this.ciudad = ciudad;
	}
	
	
	
}
